package com.studypals.global.file.entity;

/**
 * 이미지 파일의 업로드 상태를 나타내는 열거형(Enum) 클래스입니다.
 * <p>
 * 클라이언트가 Presigned URL을 발급받은 시점부터 실제 파일 업로드가 완료되거나 실패하기까지의
 * 이미지 파일 라이프사이클을 관리하는 데 사용됩니다.
 *
 * @author sleepyhoon
 * @since 2024-01-15
 */
public enum ImageStatus {
    /**
     * Presigned URL이 발급되었으나, 아직 파일이 스토리지에 최종적으로 업로드되지 않은 대기 상태입니다.
     * 이미지 메타데이터가 DB에 처음 저장될 때의 기본 상태입니다.
     */
    PENDING,

    /**
     * 파일이 스토리지에 성공적으로 업로드되고, 서버가 이를 확인한 완료 상태입니다.
     * 이 상태의 이미지만 사용자에게 정상적으로 노출됩니다.
     */
    COMPLETE,

    /**
     * Presigned URL의 유효 기간이 만료되거나 다른 이유로 파일 업로드가 실패한 상태입니다.
     * 스케줄링된 배치 작업을 통해 이 상태의 이미지 메타데이터는 주기적으로 정리될 수 있습니다.
     */
    EXPIRED
}
